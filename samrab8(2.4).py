import math

"""

Литовченко Даниил, ИВТ 2 группа 3 подгруппа

Вариант 6:
Площадь поверхности куба равна s. Найдите его диагональ.
Решение задачи оформите в виде функции diagonal(s), которая будет возвращать d.
Например, при s=18 функция diagonal(18) вернет значение переменной d,
которая равна 3

"""

def diagonal(s):
    a = math.sqrt(s/6)
    d = math.sqrt(3*a*a)
    return d

s=float(input("Введите площадь поверхности: "))
print("Диагональ: ",diagonal(s))
